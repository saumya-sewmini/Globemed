    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int patientId = LoggedInUser.getUser().getUserId();

        Patient patient = new Patient();
        patient.setPatientId((int) patientId);

        String selectedDoctorName = (String) jComboBox1.getSelectedItem();

        if (selectedDoctorName == null) {
            JOptionPane.showMessageDialog(this, "Please select a doctor!");
            return;
        }

        Doctor selectedDoctor = new DoctorDAO().findByDisplayName(selectedDoctorName);
        if (selectedDoctor == null) {
            JOptionPane.showMessageDialog(this, "Selected doctor not found!");
            return;
        }

        Date selectedDate = dateChooser.getDate();
        LocalDateTime localDateTime = selectedDate.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDateTime();

        String notes = txtNotes.getText();

        Status defaultStatus = new Status();
        defaultStatus.setId(1);

        Appointment appointment = new AppointmentBuilder()
                .setPatient(patient)
                .setDoctor(selectedDoctor)
                .setDate(localDateTime)
                .setStatus(defaultStatus)
                .setNotes(notes)
                .build();

        new AppointmentDAO().save(appointment);

        JOptionPane.showMessageDialog(this, "Appointment booked successfully!");
    }//GEN-LAST:event_jButton1ActionPerformed

