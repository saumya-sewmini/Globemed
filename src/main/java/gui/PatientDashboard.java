    private void loadDoctorsIntoCombo() {

        jComboBox1.removeAllItems();

        List<Doctor> doctors = new DoctorDAO().findAll();

        for (Doctor doctor : doctors) {
            String displayName = doctor.getFname() + " " + doctor.getLname() + " (" + doctor.getSpecialization() + ")";
            jComboBox1.addItem(displayName);
        }
    }

    private void loadAppointments() {
        int patientId = LoggedInUser.getUser().getUserId();
        AppointmentDAO dao = new AppointmentDAO();
        List<Appointment> appointments = dao.findByPatientId(patientId);

        DefaultTableModel model = (DefaultTableModel) tblAppointments.getModel();
        model.setRowCount(0); // clear existing rows

        for (Appointment a : appointments) {
            model.addRow(new Object[]{
                a.getId(),
                a.getDoctor().getFname() +" "+a.getDoctor().getLname(),
                a.getAppointmentDate(),
                a.getStatus().getStatus(),
                a.getNotes()
            });
        }
    }

